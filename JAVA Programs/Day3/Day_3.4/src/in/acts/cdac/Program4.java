public class Program4 {

	public static void main(String[] args) {
		int num1 = 10;
		
		double num2 = num1;
		
		float num3 = num1;
		
		boolean bool1 = num1 != 0;
		
		String str1 = Integer.toString(num1);
		
		
		
		System.out.println("Integer = Num1 : " +num1);
		System.out.println("Integer to Double conversion = Num2 : " +num2);
		System.out.println("Integer to Float conversion = Num3 : " +num3);
		System.out.println("Integer to Boolean conversion = Num3 : " +bool1);
		System.out.println("Integer to String conversion = Num4 : " +str1);

	}

}
